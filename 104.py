# sentence into list of words
sentence = "Python is very powerful"

# Split sentence into words
words = sentence.split()

print(words)

"""
sentence.split()

Splits the string wherever there is whitespace (spaces, tabs, newlines).

Returns a list of words.
"""

# You can also split by specific characters
text = "apple,banana,mango"

fruits = text.split(",")

print(fruits)